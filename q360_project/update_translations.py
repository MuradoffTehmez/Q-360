def update_po_file(po_file_path):
    """Update the .po file with English translations for empty msgstr entries."""
    with open(po_file_path, 'r', encoding='utf-8') as file:
        content = file.read()
    
    # Find all entries with empty msgstr
    entries = re.finditer(r'(\n#: .+\n)?msgid "(.*?)"\nmsgstr ""', content, re.DOTALL)
    
    # Process entries in reverse order to avoid position shifting
    matches = list(entries)
    for match in reversed(matches):
        start, end = match.span()
        msgid_match = match.group(2)
        
        # Get the English translation
        en_translation = translate_azerijani_to_english(msgid_match)
        
        # Replace the empty msgstr with the English translation
        replacement = f'msgid "{msgid_match}"\nmsgstr "{en_translation}"'
        content = content[:start] + replacement + content[end:]
    
    # Write the updated content back to the file
    with open(po_file_path, 'w', encoding='utf-8') as file:
        file.write(content)
    
    print(f"Updated {po_file_path} with English translations for empty msgstr entries.")


